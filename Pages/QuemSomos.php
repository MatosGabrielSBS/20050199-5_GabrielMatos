<h1 align="center"  id="TextNS">QUEM SOMOS</h1>
<p id="textpNS">Um ideia que surgiu após a crise moveleira na cidade, a Sr. Maria Elfrida conhecida como "Vovó Elfrida" decidiu utlizar seu tempo livre ajudar seu filho com as contas com vendendo cuque para os vizinhos.A demanda começou a crescer na região e após 2 anos toda a familia se envolveu no négocio, em uma década de existencia a Alimentos Elfrida LTDA. já atende aos principais mercados da município e conta com moderna estrutura de panificação. </p>
<ul>O que produzimos:
<li id="listNS"> Cuques e nega malucas;</li>
<li id="listNS"> Pães fracês e d'água;</li>
<li id="listNS"> Orelhas e pão de queijo;</li>

